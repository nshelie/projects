  private void initCards() {
    // Existing panels
    homePanel = new AdminHomePage(userService, studentService);
    studentPage = new StudentPage(userService, studentService);
    subjectPage = new SubjectPage(subjectService);
    performancePage = new PerformancePage(performanceService, subjectService, studentService);
    careerRolePage = new CareerRolePage(careerRoleService, subjectService);
    companyPage = new CompanyPage(companyService, careerRoleService);
    recommendationPage = new RecommendationPage(careerRoleService);

    // Add panels to CardLayout
    contentPanel.add(homePanel, "home");
    contentPanel.add(studentPage, "students");
    contentPanel.add(subjectPage, "subjects");
    contentPanel.add(performancePage, "performance");
    contentPanel.add(careerRolePage, "careerRoles");
    contentPanel.add(companyPage, "companies");
    contentPanel.add(recommendationPage, "recommendations");

    // Show default card
    java.awt.CardLayout cl = (java.awt.CardLayout) contentPanel.getLayout();
    cl.show(contentPanel, "home");
}


