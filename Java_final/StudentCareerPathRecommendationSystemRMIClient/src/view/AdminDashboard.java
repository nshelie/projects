    private void logoutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutBtnActionPerformed

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to logout?",
            "Logout",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            // Close current window
            this.dispose();

            // Open login page again
            new LoginForm().setVisible(true);  // Change LoginPage to your actual login JFrame class name
        }
    }//GEN-LAST:event_logoutBtnActionPerformed

    private void applyRoleAccess() {
    if(Session.currentUserRole.equals("HOME")) {
        homeBtn.setVisible(false);
        studentBtn.setVisible(false);       // hide manage students
        subjectBtn.setVisible(false);       // hide manage subjects
        performanceBtn.setVisible(false);   // hide manage performance
        careerRoleBtn.setVisible(false);    // hide manage career roles
        companiesBtn.setVisible(false);     // hide manage companies
    }
    }
    
