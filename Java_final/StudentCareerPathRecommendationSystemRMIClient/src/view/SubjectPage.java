    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
                                       
     String subjectName = subjectNameTxt.getText().trim();
    String description = descriptionTxt.getText().trim();
    String level = levelTxt.getText().trim();
    String creditsStr = creditsTxt.getText().trim();

    // VALIDATIONS
    if (subjectName.isEmpty() || !subjectName.matches("[a-zA-Z ]+")) {
        JOptionPane.showMessageDialog(this, "Subject Name is required and must contain only letters!");
        subjectNameTxt.requestFocus();
        return;
    }

    if (description.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Description is required!");
        descriptionTxt.requestFocus();
        return;
    }

    if (level.isEmpty() || !level.matches("(year [1-4]|semester [1-9]|semester 10)")) {
        JOptionPane.showMessageDialog(this, "Level must be 'year 1-4' or 'semester 1-10'");
        levelTxt.requestFocus();
        return;
    }

    int credits;
    try {
        credits = Integer.parseInt(creditsStr);
        if (credits <= 0) {
            JOptionPane.showMessageDialog(this, "Credits must be a positive number!");
            creditsTxt.requestFocus();
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Credits must be a valid number!");
        creditsTxt.requestFocus();
        return;
    }

    // CREATE SUBJECT OBJECT
    Subject s = new Subject();
    s.setSubjectName(subjectName);
    s.setDescription(description);
    s.setLevel(level);
    s.setCredits(credits);

    // SAVE TO DATABASE USING SERVICE
    try {
        Subject saved = subjectService.saveSubject(s);
        if (saved != null) {
            JOptionPane.showMessageDialog(this, "Subject added successfully!");
            loadSubjectTable();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add subject.");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_addBtnActionPerformed

