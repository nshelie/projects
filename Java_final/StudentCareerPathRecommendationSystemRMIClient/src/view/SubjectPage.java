    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
     String searchStr = searchTXt.getText().trim();
    if (searchStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a Subject ID to search!");
        return;
    }

    try {
        int id = Integer.parseInt(searchStr);
        Subject s = subjectService.searchSubjectById(id);

        if (s != null) {
            subjectIdTxt.setText(String.valueOf(s.getSubjectId()));
            subjectNameTxt.setText(s.getSubjectName());
            descriptionTxt.setText(s.getDescription());
            levelTxt.setText(s.getLevel());
            creditsTxt.setText(String.valueOf(s.getCredits()));
        } else {
            JOptionPane.showMessageDialog(this, "Subject not found.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid ID format");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_searchBtnActionPerformed

