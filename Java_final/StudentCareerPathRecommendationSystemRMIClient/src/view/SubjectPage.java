    private void loadSubjectTable() {
 try {
    List<Subject> list = subjectService.retrieveAllSubjects();
    DefaultTableModel model = (DefaultTableModel) subjectTable.getModel();
    model.setRowCount(0);

    for (Subject s : list) {
        model.addRow(new Object[]{
            s.getSubjectId(),
            s.getSubjectName(),
            s.getDescription(),
            s.getLevel(),
            s.getCredits()
        });
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Failed to load subjects: " + e.getMessage());
}

}
