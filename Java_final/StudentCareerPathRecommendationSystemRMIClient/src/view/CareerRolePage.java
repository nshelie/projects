    private void loadSubjects() {
    requiredSubjectsCombo.removeAllItems();
    try {
        List<Subject> subjects = subjectService.retrieveAllSubjects();
        for (Subject s : subjects) {
            requiredSubjectsCombo.addItem(s.getSubjectId() + " - " + s.getSubjectName());
        }
    } catch (RemoteException e) {
        JOptionPane.showMessageDialog(this, "Error loading subjects: " + e.getMessage());
    }
}

private void loadCareerRoles() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    try {
        List<CareerRoleDTO> roles = careerRoleService.retrieveAllCareerRoles();

        for (CareerRoleDTO cr : roles) {
            String subjectsText = "Unknown";
            if (cr.getRequiredSubjectNames() != null && !cr.getRequiredSubjectNames().isEmpty()) {
                StringBuilder sb = new StringBuilder();
                for (String n : cr.getRequiredSubjectNames()) {
                    if (n == null || n.trim().isEmpty()) continue;
                    if (sb.length() > 0) sb.append(", ");
                    sb.append(n.trim());
                }
                if (sb.length() > 0) subjectsText = sb.toString();
            }

            model.addRow(new Object[]{
                cr.getRoleId(),
                cr.getRoleName(),
                cr.getDescription(),
                subjectsText,
                cr.getRequiredMinScore()
            });
        }

    } catch (RemoteException e) {
        JOptionPane.showMessageDialog(this, "Error loading career roles: " + e.getMessage());
    }
}



