    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
                                      
    try {
        // --- GET FORM VALUES ---
        String roleName = roleNameTxt.getText().trim();
        String description = descriptionTxt.getText().trim();
        String minScoreStr = minScoreTxt.getText().trim();
        String selectedSubjectStr = (String) requiredSubjectsCombo.getSelectedItem();

        // --- VALIDATIONS ---
        if (roleName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Role Name cannot be empty.");
            return;
        }
        if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Description cannot be empty.");
            return;
        }
        if (minScoreStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Minimum score cannot be empty.");
            return;
        }

        int minScore;
        try {
            minScore = Integer.parseInt(minScoreStr);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Minimum score must be a valid number.");
            return;
        }
        if (minScore < 60) {
            JOptionPane.showMessageDialog(this, "Minimum score must be at least 60.");
            return;
        }
        if (selectedSubjectStr == null || selectedSubjectStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a required subject.");
            return;
        }

        // --- Convert selected subject string to ID + Name ---
        String[] parts = selectedSubjectStr.split(" - ", 2);
        int subjectId = Integer.parseInt(parts[0]);
        String subjectName = (parts.length > 1) ? parts[1].trim() : "";

        List<Integer> subjectIds = new ArrayList<>();
        subjectIds.add(subjectId);
        List<String> subjectNames = new ArrayList<>();
        subjectNames.add(subjectName);

        // --- CREATE CareerRoleDTO ---
        CareerRoleDTO cr = new CareerRoleDTO();
        cr.setRoleName(roleName);
        cr.setDescription(description);
        cr.setRequiredMinScore(minScore);
        cr.setRequiredSubjectIds(subjectIds);
        cr.setRequiredSubjectNames(subjectNames);

        // --- CALL SERVICE TO ADD ---
        careerRoleService.addCareerRole(cr); // RMI call
        JOptionPane.showMessageDialog(this, "Career role added successfully!");

        clearForm();
        loadCareerRoles();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error adding career role: " + e.getMessage());
    }
    }//GEN-LAST:event_addBtnActionPerformed

