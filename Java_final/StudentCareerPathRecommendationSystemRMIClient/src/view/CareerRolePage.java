    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
                                          
    try {
        if (searchTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Role ID to search.");
            return;
        }

        int searchId = Integer.parseInt(searchTxt.getText().trim());
        CareerRoleDTO cr = careerRoleService.findCareerRoleById(searchId); // RMI call

        if (cr != null) {
            roleIdTxt.setText(String.valueOf(cr.getRoleId()));
            roleNameTxt.setText(cr.getRoleName());
            descriptionTxt.setText(cr.getDescription());
            minScoreTxt.setText(String.valueOf(cr.getRequiredMinScore()));

            // --- Select first required subject (if any) ---
            if (cr.getRequiredSubjectIds() != null && !cr.getRequiredSubjectIds().isEmpty()) {
                int subjectId = cr.getRequiredSubjectIds().get(0);
                for (int i = 0; i < requiredSubjectsCombo.getItemCount(); i++) {
                    String item = requiredSubjectsCombo.getItemAt(i);
                    if (item == null) continue;
                    String[] parts = item.split(" - ", 2);
                    if (parts.length == 0) continue;
                    try {
                        int itemId = Integer.parseInt(parts[0]);
                        if (itemId == subjectId) {
                            requiredSubjectsCombo.setSelectedIndex(i);
                            break;
                        }
                    } catch (NumberFormatException ignore) {
                        // ignore
                    }
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Career role not found!");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid Role ID.");
    } catch (RemoteException e) {
        JOptionPane.showMessageDialog(this, "Error searching career role: " + e.getMessage());
    }
    }//GEN-LAST:event_searchBtnActionPerformed

