    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
                                         
    try {
        if (roleIdTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a career role first.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this career role?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        int roleId = Integer.parseInt(roleIdTxt.getText().trim());
        CareerRoleDTO cr = new CareerRoleDTO();
        cr.setRoleId(roleId);

        CareerRoleDTO deleted = careerRoleService.deleteCareerRole(cr);
        if (deleted != null) {
            JOptionPane.showMessageDialog(this, "Career role deleted successfully!");
            clearForm();
            loadCareerRoles();
        }

    } catch (RemoteException e) {
        JOptionPane.showMessageDialog(this, "Error deleting career role: " + e.getMessage());
    }
    }//GEN-LAST:event_deleteBtnActionPerformed

