    private void loadStudentInfo() {
        if (Session.currentUser != null) {
            jLabel2.setText("Student Name: " + Session.currentUser.getFullName());
            jLabel3.setText("Department: " + Session.currentUser.getDepartment());
        }
    }

    private void loadRecommendations() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        int studentId = Session.currentStudentId;

        try {
            List<CareerRoleDTO> recommendations = careerRoleService.getRecommendationsForStudent(studentId);

            if (recommendations == null || recommendations.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No recommendations available based on your subjects and scores.");
                return;
            }

            for (CareerRoleDTO r : recommendations) {
                String skills = "-";
                if (r.getRequiredSubjectNames() != null && !r.getRequiredSubjectNames().isEmpty()) {
                    skills = String.join(", ", r.getRequiredSubjectNames());
                }

                // There's no explicit match score in the DTO. Show the required minimum score instead.
                String matchScore = String.valueOf(r.getRequiredMinScore());

                model.addRow(new Object[]{
                    r.getRoleName(),
                    r.getDescription(),
                    skills,
                    matchScore
                });
            }

        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, "Failed to load recommendations: " + ex.getMessage());
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to load recommendations: " + ex.getMessage());
        }
    }

