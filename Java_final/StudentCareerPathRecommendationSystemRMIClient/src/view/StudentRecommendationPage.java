    private void detailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailsBtnActionPerformed

        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a career first!");
            return;
        }

        String careerName = jTable1.getValueAt(row, 0).toString();
        String description = jTable1.getValueAt(row, 1).toString();
        String skills = jTable1.getValueAt(row, 2).toString();
        String matchScore = jTable1.getValueAt(row, 3).toString();

        JOptionPane.showMessageDialog(this,
            "Career Name: " + careerName +
            "\nDescription: " + description +
            "\nSkills Needed: " + skills +
            "\nMatch Score: " + matchScore,
            "Career Details",
            JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_detailsBtnActionPerformed


