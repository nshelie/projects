    public RecommendationPage(CareerRoleService careerRoleService) {
        this.careerRoleService = careerRoleService;
        initComponents();

        // Load careers on open
        jList1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        // Bind to the NetBeans form JTable named `studentTable`
        studentsTable = studentTable;
        if (studentsTable != null && studentsTable.getModel() instanceof DefaultTableModel) {
            studentsTableModel = (DefaultTableModel) studentsTable.getModel();
        }

        loadCareerList(null);

        // When a career is selected, load the recommended students for it
        jList1.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (e.getValueIsAdjusting()) return;
                String selected = jList1.getSelectedValue();
                if (selected == null || selected.trim().isEmpty()) return;

                // selected format: "<roleId> - <roleName>"
                String[] parts = selected.split("\\s*-\\s*", 2);
                if (parts.length == 0) return;

                try {
                    int roleId = Integer.parseInt(parts[0].trim());
                    loadStudentsForCareer(roleId);
                } catch (NumberFormatException ignore) {
                    // ignore
                }
            }
        });
    }

