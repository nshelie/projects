    private void loadCareerList(String keyword) {
        DefaultListModel<String> careersModel = new DefaultListModel<>();

        // Clear students table when reloading careers
        if (studentsTableModel != null) {
            studentsTableModel.setRowCount(0);
        }

        if (careerRoleService == null) {
            JOptionPane.showMessageDialog(this, "CareerRoleService not connected.");
            jList1.setModel(careersModel);
            return;
        }

        try {
            cachedRoles = careerRoleService.retrieveAllCareerRoles();
            if (cachedRoles == null) cachedRoles = new ArrayList<>();

            String k = (keyword == null) ? "" : keyword.trim().toLowerCase();

            for (CareerRoleDTO cr : cachedRoles) {
                if (cr == null) continue;

                if (!k.isEmpty()) {
                    boolean match = String.valueOf(cr.getRoleId()).contains(k)
                            || (cr.getRoleName() != null && cr.getRoleName().toLowerCase().contains(k));
                    if (!match) continue;
                }

                careersModel.addElement(cr.getRoleId() + " - " + cr.getRoleName());
            }

            jList1.setModel(careersModel);

            // Auto-select first item (optional, but improves UX)
            if (careersModel.getSize() > 0) {
                jList1.setSelectedIndex(0);
            }

        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, "Failed to load careers: " + ex.getMessage());
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to load careers: " + ex.getMessage());
        }
    }

    private void loadStudentsForCareer(int roleId) {
        if (studentsTableModel != null) {
            studentsTableModel.setRowCount(0);
        }

        if (careerRoleService == null) {
            JOptionPane.showMessageDialog(this, "CareerRoleService not connected.");
            return;
        }

        try {
            List<RecommendedStudentDTO> students = careerRoleService.getRecommendedStudentsForCareer(roleId);

            if (students == null || students.isEmpty()) {
                return;
            }

            // Already sorted DESC by score on server
            int i = 1;
            for (RecommendedStudentDTO s : students) {
                String studentLabel = s.getStudentId() + " - " + s.getFullName();
                studentsTableModel.addRow(new Object[]{
                    i++,
                    studentLabel,
                    s.getScore()
                });
            }

        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, "Failed to load students: " + ex.getMessage());
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to load students: " + ex.getMessage());
        }
    }

