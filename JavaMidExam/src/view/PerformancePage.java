        try {
            int id = Integer.parseInt(performanceIdTxt.getText());
            int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete this performance?",
                "Delete",
                JOptionPane.YES_NO_OPTION
            );
            if(confirm == JOptionPane.YES_OPTION) {
                if(dao.deletePerformance(id)) {
                    JOptionPane.showMessageDialog(this, "Performance deleted successfully!");
                    clearFields();
                    loadTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete performance.");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please select a valid Performance record.");
        }
