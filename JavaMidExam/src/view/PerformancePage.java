         private void loadStudentAndSubject() {
    // Load students
    List<Student> students = new StudentDao().findAllStudents();
    studentCombo.removeAllItems();
    for(Student s : students){
        studentCombo.addItem(s.getStudentId() + " - " + s.getName());
    }

    // Load subjects
    List<Subject> subjects = new SubjectDao().findAllSubjects();
    subjectCombo.removeAllItems();
    for(Subject s : subjects){
        subjectCombo.addItem(s.getSubjectId() + " - " + s.getSubjectName());
    }
}
      
    private String calculateGrade(double score) {
    if(score >= 90) return "A";
    else if(score >= 80) return "B";
    else if(score >= 70) return "C";
    else if(score >= 60) return "D";
    else return "F";
}

// Clear input fields and reload combos
private void clearFields() {
    performanceIdTxt.setText("");    
    scoreTxt.setText("");
    gradeTxt.setText("");
    searchTxt.setText("");
    loadStudentAndSubject();
    
}



private void loadTable() {
    List<Performance> list = dao.findAll();
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // clear previous rows

    StudentDao studentDao = new StudentDao();
    SubjectDao subjectDao = new SubjectDao();

    for(Performance p : list) {
        // Get Student Name
        Student student = studentDao.findById(p.getStudentId());
        String studentName = (student != null) ? student.getName() : "Unknown";

        // Get Subject Name
        Subject subject = subjectDao.findById(p.getSubjectId());
        String subjectName = (subject != null) ? subject.getSubjectName() : "Unknown";

        // Add row with names
        model.addRow(new Object[]{
            p.getPerformanceId(),
            studentName,
            subjectName,
            p.getScore(),
            p.getGrade()
        });
    }
}

