    private void searchBtn4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtn4ActionPerformed

        if (searchTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Performance ID to search.");
            return;
        }

        try {
            int id = Integer.parseInt(searchTxt.getText());
            Performance p = dao.findById(id);

            if(p != null) {
                performanceIdTxt.setText(String.valueOf(p.getPerformanceId()));

                // Select Student in ComboBox
                for (int i = 0; i < studentCombo.getItemCount(); i++) {
                    String item = studentCombo.getItemAt(i);
                    int studentId = Integer.parseInt(item.split(" - ")[0]);
                    if (studentId == p.getStudentId()) {
                        studentCombo.setSelectedIndex(i);
                        break;
                    }
                }

                // Select Subject in ComboBox
                for (int i = 0; i < subjectCombo.getItemCount(); i++) {
                    String item = subjectCombo.getItemAt(i);
                    int subjectId = Integer.parseInt(item.split(" - ")[0]);
                    if (subjectId == p.getSubjectId()) {
                        subjectCombo.setSelectedIndex(i);
                        break;
                    }
                }

                scoreTxt.setText(String.valueOf(p.getScore()));
                gradeTxt.setText(p.getGrade());
            } else {
                JOptionPane.showMessageDialog(this, "Performance not found!");
                clearFields();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a valid Performance ID to search.");
        }
    }//GEN-LAST:event_searchBtn4ActionPerformed

    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        try {
            // Validate Student selection
            if (studentCombo.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Please select a Student.");
                return;
            }

            // Validate Subject selection
            if (subjectCombo.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Please select a Subject.");
                return;
            }

            // Validate Score
            if (scoreTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter the Score.");
                return;
            }

            double score = Double.parseDouble(scoreTxt.getText());
            if (score < 0 || score > 100) {
                JOptionPane.showMessageDialog(this, "Score must be between 0 and 100.");
                return;
            }

            // Get IDs from combobox
            int studentId = Integer.parseInt(((String) studentCombo.getSelectedItem()).split(" - ")[0]);
            int subjectId = Integer.parseInt(((String) subjectCombo.getSelectedItem()).split(" - ")[0]);

            // Check duplicate performance
            if (dao.exists(studentId, subjectId)) {
                JOptionPane.showMessageDialog(this, "This student already has a score for this subject.");
                return;
            }

            // Calculate grade
            String grade = calculateGrade(score);
            gradeTxt.setText(grade);

            // Insert performance
            if (dao.addPerformanceAndStudentSubject(studentId, subjectId, (int) score)) {
                JOptionPane.showMessageDialog(this, "Performance added successfully!");
                clearFields();
                loadTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add performance. Try again.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Score must be a valid number.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_registerBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        try {
            // Validate Performance selection
            if (performanceIdTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please search and select a performance before updating.");
                return;
            }

            // Validate Student selection
            if (studentCombo.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Please select a Student.");
                return;
            }

            // Validate Subject selection
            if (subjectCombo.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Please select a Subject.");
                return;
            }

            // Validate Score
            if (scoreTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter the Score.");
                return;
            }

            double score = Double.parseDouble(scoreTxt.getText());
            if (score < 0 || score > 100) {
                JOptionPane.showMessageDialog(this, "Score must be between 0 and 100.");
                return;
            }

            // Create Performance object
            Performance p = new Performance();
            p.setPerformanceId(Integer.parseInt(performanceIdTxt.getText()));
            int studentId = Integer.parseInt(((String) studentCombo.getSelectedItem()).split(" - ")[0]);
            int subjectId = Integer.parseInt(((String) subjectCombo.getSelectedItem()).split(" - ")[0]);
            p.setStudentId(studentId);
            p.setSubjectId(subjectId);
            p.setScore(score);
            p.setGrade(calculateGrade(score));
            gradeTxt.setText(p.getGrade());

            // Check for duplicate if student/subject changed
            Performance old = dao.findById(p.getPerformanceId());
            if(old != null && (old.getStudentId() != studentId || old.getSubjectId() != subjectId)) {
                if(dao.exists(studentId, subjectId)) {
                    JOptionPane.showMessageDialog(this, "This student already has a score for this subject.");
                    return;
                }
            }

            // Update in DB
            if (dao.updatePerformance(p)) {
                JOptionPane.showMessageDialog(this, "Performance updated successfully!");
                clearFields();
                loadTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update performance. Try again.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Score must be a valid number.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

