    private void addTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTxtActionPerformed
                                       
                                      
    String subjectName = subjectNameTxt.getText().trim();
    String description = descriptionTxt.getText().trim();
    String level = levelTxt.getText().trim();
    String creditsStr = creditsTxt.getText().trim();

    // VALIDATIONS
    if (subjectName.isEmpty() || !subjectName.matches("[a-zA-Z ]+")) {
        JOptionPane.showMessageDialog(this, "Subject Name is required and must contain only letters!");
        subjectNameTxt.requestFocus();
        return;
    }

    // ðŸ”¥ NEW: check if subject name already exists
    SubjectDao dao = new SubjectDao(); // moved up
    if (dao.subjectNameExists(subjectName)) {
        JOptionPane.showMessageDialog(this, "This subject name already exists. Please choose another name.");
        subjectNameTxt.requestFocus();
        return;
    }

    if (description.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Description is required!");
        descriptionTxt.requestFocus();
        return;
    }

    if (level.isEmpty() || !level.matches("(year [1-4]|semester [1-9]|semester 10)")) {
        JOptionPane.showMessageDialog(this, "Level must be 'year 1-4' or 'semester 1-10'");
        levelTxt.requestFocus();
        return;
    }

    int credits;
    try {
        credits = Integer.parseInt(creditsStr);
        if (credits <= 0) {
            JOptionPane.showMessageDialog(this, "Credits must be a positive number!");
            creditsTxt.requestFocus();
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Credits must be a valid number!");
        creditsTxt.requestFocus();
        return;
    }

    // CREATE SUBJECT OBJECT
    Subject s = new Subject();
    s.setSubjectName(subjectName);
    s.setDescription(description);
    s.setLevel(level);
    s.setCredits(credits);

    // SAVE TO DATABASE
    int result = dao.createSubject(s);  // reuse same dao

    if (result > 0) {
        JOptionPane.showMessageDialog(this, "Subject added successfully!");
        loadSubjectTable();
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, "Failed to add subject.");
    }

    }//GEN-LAST:event_addTxtActionPerformed

    private void updateTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTxtActionPerformed
                                         

    String subjectIdStr = subjectIdTxt.getText().trim();
    String subjectName = subjectNameTxt.getText().trim();
    String description = descriptionTxt.getText().trim();
    String level = levelTxt.getText().trim();
    String creditsStr = creditsTxt.getText().trim();

    // VALIDATIONS
    if (subjectIdStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a subject to update!");
        return;
    }

    if (subjectName.isEmpty() || !subjectName.matches("[a-zA-Z ]+")) {
        JOptionPane.showMessageDialog(this, "Subject Name is required and must contain only letters!");
        subjectNameTxt.requestFocus();
        return;
    }

    int subjectId = Integer.parseInt(subjectIdStr);
    SubjectDao dao = new SubjectDao();

    // ðŸ”¥ NEW: check if another subject already uses this name
    if (dao.subjectNameExistsForOtherId(subjectName, subjectId)) {
        JOptionPane.showMessageDialog(this, "Another subject already has this name. Please choose a different name.");
        subjectNameTxt.requestFocus();
        return;
    }

    if (description.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Description is required!");
        descriptionTxt.requestFocus();
        return;
    }

    if (level.isEmpty() || !level.matches("(year [1-4]|semester [1-9]|semester 10)")) {
        JOptionPane.showMessageDialog(this, "Level must be 'year 1-4' or 'semester 1-10'");
        levelTxt.requestFocus();
        return;
    }

    int credits;
    try {
        credits = Integer.parseInt(creditsStr);
        if (credits <= 0) {
            JOptionPane.showMessageDialog(this, "Credits must be a positive number!");
            creditsTxt.requestFocus();
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Credits must be a valid number!");
        creditsTxt.requestFocus();
        return;
    }

    // UPDATE SUBJECT OBJECT
    Subject s = new Subject();
    s.setSubjectId(subjectId);
    s.setSubjectName(subjectName);
    s.setDescription(description);
    s.setLevel(level);
    s.setCredits(credits);

    // UPDATE DATABASE
    int result = dao.updateSubject(s);

    if (result > 0) {
        JOptionPane.showMessageDialog(this, "Subject updated successfully!");
        loadSubjectTable();
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, "Failed to update subject.");
    }

    }//GEN-LAST:event_updateTxtActionPerformed

    private void deleteTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteTxtActionPerformed

        int id = Integer.parseInt(subjectIdTxt.getText());

        SubjectDao dao = new SubjectDao();
        int result = dao.deleteSubject(id);

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Subject deleted successfully!");
            loadSubjectTable();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete subject.");
        }
    }//GEN-LAST:event_deleteTxtActionPerformed

