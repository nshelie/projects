    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed

        try {
            int id = Integer.parseInt(searchTXt.getText());

            SubjectDao dao = new SubjectDao();
            Subject s = dao.findSubjectById(id);

            if (s != null) {
                subjectIdTxt.setText(String.valueOf(s.getSubjectId()));
                subjectNameTxt.setText(s.getSubjectName());
                descriptionTxt.setText(s.getDescription());
                levelTxt.setText(s.getLevel());
                creditsTxt.setText(String.valueOf(s.getCredits()));
            } else {
                JOptionPane.showMessageDialog(this, "Subject not found.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Invalid ID format");
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void loadSubjectTable() {
    SubjectDao dao = new SubjectDao();
    List<Subject> list = dao.findAllSubjects();

    DefaultTableModel model = (DefaultTableModel) subjectTable.getModel();
    model.setRowCount(0);

    for (Subject s : list) {
        model.addRow(new Object[]{
            s.getSubjectId(),
            s.getSubjectName(),
            s.getDescription(),
            s.getLevel(),
            s.getCredits()
        });
    }
}
