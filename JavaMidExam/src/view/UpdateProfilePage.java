     private void loadUserData() {
    if (Session.currentUser != null) {
        fullNameTxt.setText(Session.currentUser.getFullName());
        emailTxt.setText(Session.currentUser.getEmail());
        emailTxt.setEditable(false);

        // Load departments dynamically
        departmentCombo.removeAllItems();
        for (String dept : studentDao.getAllDepartments()) {
            departmentCombo.addItem(dept);
        }

        // Select current user's department
        departmentCombo.setSelectedItem(Session.currentUser.getDepartment());
    } else {
        JOptionPane.showMessageDialog(this, "No user is logged in!");
    }
}
     
