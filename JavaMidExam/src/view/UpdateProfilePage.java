    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed

        String fullName = fullNameTxt.getText().trim();
        String password = new String(passwordTxt.getPassword());
        String department = departmentCombo.getSelectedItem().toString();

        if (fullName.isEmpty() || password.isEmpty() || department.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields");
            return;
        }

        int userId = Session.currentUserId;
        String email = Session.currentUserEmail;

        // Update Student table
        Student s = new Student();
        s.setStudentId(userId);           // assuming student_id = user_id
        s.setFullName(fullName);
        s.setPassword(password);
        s.setEmail(email);
        s.setDepartment(department);       // important: set department

        int rows = studentDao.updateStudent(s);
        boolean studentUpdated = rows > 0;

        // Update User table
        User u = new User();
        u.setUserId(userId);
        u.setFullName(fullName);
        u.setPassword(password);
        u.setEmail(email);
        u.setRole(Session.currentUserRole);

        boolean userUpdated = UserDao.updateUser(u);

        if (studentUpdated && userUpdated) {
            // update session too
            Session.currentUserName = fullName;
            Session.currentUser.setDepartment(department);
            Session.currentUser.setFullName(fullName);
            Session.currentUser.setPassword(password);

            JOptionPane.showMessageDialog(this, "Profile updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update profile!");
        }
    }//GEN-LAST:event_updateBtnActionPerformed


