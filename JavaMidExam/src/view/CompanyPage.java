          private void loadCareerRoles() {
        careerRoleCombo.removeAllItems();
        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/student_career_path_recommendation_system_db",
                "root", "password123")) {

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT role_id, role_name FROM career_role");
            while (rs.next()) {
                careerRoleCombo.addItem(rs.getInt("role_id") + " - " + rs.getString("role_name"));
            }
            rs.close();
            st.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

private void showCompaniesInTable() {
    List<Company> companies = CompanyDao.getAllCompanies();
    String[] columns = {"ID", "Company Name", "Career Role", "Location", "Description"}; // renamed header
    Object[][] data = new Object[companies.size()][5];

    for (int i = 0; i < companies.size(); i++) {
        Company c = companies.get(i);
        data[i][0] = c.getCompanyId();
        data[i][1] = c.getCompanyName();
        // get role name instead of ID
        data[i][2] = getCareerRoleName(c.getCareerRoleId());
        data[i][3] = c.getLocation();
        data[i][4] = c.getDescription();
    }

    jTable1.setModel(new javax.swing.table.DefaultTableModel(data, columns) {
        public boolean isCellEditable(int row, int col) {
            return false; // make table readonly
        }
    });
}

