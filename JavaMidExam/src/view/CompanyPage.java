    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
                                       
    try {
        String name = companyNameTxt.getText().trim();
        String loc = locationTxt.getText().trim();
        String desc = descriptionTxt.getText().trim();

        // basic validation
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Company name is required.");
            companyNameTxt.requestFocus();
            return;
        }

        if (careerRoleCombo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select a career role.");
            return;
        }

        int careerRoleId = Integer.parseInt(
                careerRoleCombo.getSelectedItem().toString().split(" - ")[0]);

        // ðŸ”´ VALIDATION: same company + same career role cannot be saved again
        if (CompanyDao.existsByNameAndRole(name, careerRoleId)) {
            JOptionPane.showMessageDialog(this,
                    "This company is already registered with this career role.\n" +
                    "You cannot assign the same career role to the same company twice.");
            return;
        }

        Company c = new Company(0, name, careerRoleId, loc, desc);
        if (CompanyDao.addCompany(c)) {
            showCompaniesInTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Company added successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add company.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error adding company: " + e.getMessage());
    }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
                                         
    try {
        if (companyIdTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please search or select a company first.");
            return;
        }

        int id = Integer.parseInt(companyIdTxt.getText().trim());
        String name = companyNameTxt.getText().trim();
        String loc = locationTxt.getText().trim();
        String desc = descriptionTxt.getText().trim();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Company name is required.");
            companyNameTxt.requestFocus();
            return;
        }

        if (careerRoleCombo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select a career role.");
            return;
        }

        int careerRoleId = Integer.parseInt(
                careerRoleCombo.getSelectedItem().toString().split(" - ")[0]);

        // ðŸ”´ VALIDATION: on update, prevent duplicate company + role (excluding this company id)
        if (CompanyDao.existsByNameAndRoleExceptId(name, careerRoleId, id)) {
            JOptionPane.showMessageDialog(this,
                    "Another record already has this same company name with this career role.\n" +
                    "You cannot duplicate company + career role.");
            return;
        }

        Company c = new Company(id, name, careerRoleId, loc, desc);
        if (CompanyDao.updateCompany(c)) {
            showCompaniesInTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Company updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update company.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error updating company: " + e.getMessage());
    }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        try {
            if (companyIdTxt.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please search or select a company first.");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this company?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);

            if (confirm != JOptionPane.YES_OPTION) return;

            int id = Integer.parseInt(companyIdTxt.getText().trim());
            if (CompanyDao.deleteCompany(id)) {
                showCompaniesInTable();
                clearForm();
                JOptionPane.showMessageDialog(this, "Company deleted successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete company.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error deleting company: " + e.getMessage());
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed

        try {
            if (searchTxt.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Company ID to search.");
                return;
            }

            int id = Integer.parseInt(searchTxt.getText().trim());
            Company c = CompanyDao.getCompanyById(id);
            if (c != null) {
                companyIdTxt.setText(String.valueOf(c.getCompanyId()));
                companyNameTxt.setText(c.getCompanyName());
                locationTxt.setText(c.getLocation());
                descriptionTxt.setText(c.getDescription());

                int roleId = c.getCareerRoleId();
                String roleName = getCareerRoleName(roleId);
                String comboValue = roleId + " - " + roleName;

                for (int i = 0; i < careerRoleCombo.getItemCount(); i++) {
                    if (careerRoleCombo.getItemAt(i).equals(comboValue)) {
                        careerRoleCombo.setSelectedIndex(i);
                        break;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Company not found!");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid ID format.");
        }
        }

        private String getCareerRoleName(int id) {
            String roleName = "";
            try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/student_career_path_recommendation_system_db",
                "root", "password123")) {

            PreparedStatement ps = con.prepareStatement("SELECT role_name FROM career_role WHERE role_id=?");
            ps.setInt(1, id);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                roleName = rs.getString("role_name");
            }
            rs.close();
            ps.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return roleName;
    }//GEN-LAST:event_searchBtnActionPerformed

