     private void loadProfitLoss() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        double totalIncome = 0;
        double totalExpense = 0;
        double totalProfitLoss = 0;

       String sql = "SELECT a.apartment_number, "
           + "COALESCE(SUM(p.amount_paid), 0) AS income, "
           + "COALESCE(SUM(e.amount), 0) AS expense, "
           + "COALESCE(SUM(p.amount_paid), 0) - COALESCE(SUM(e.amount), 0) AS profit_loss "
           + "FROM apartment a "
           + "LEFT JOIN payment p ON a.id = p.apartment_id "
           + "LEFT JOIN expense e ON a.id = e.apartment_id "
           + "GROUP BY a.id, a.apartment_number";

        try (Connection conn = DBConnection.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                String apt = rs.getString("apartment_number");
                double income = rs.getDouble("income");
                double expense = rs.getDouble("expense");
                double profitLoss = rs.getDouble("profit_loss");

                totalIncome += income;
                totalExpense += expense;
                totalProfitLoss += profitLoss;

                model.addRow(new Object[]{apt, income, expense, profitLoss});
            }

            // Add TOTAL row
            model.addRow(new Object[]{"TOTAL", totalIncome, totalExpense, totalProfitLoss});

        } catch (SQLException ex) {
            System.out.println("Load Profit/Loss Error: " + ex.getMessage());
        }
    }
     
