private void loadApartments() {
    try (Connection conn = DBConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement("SELECT id, apartment_number FROM apartment")) {

        ResultSet rs = ps.executeQuery();
        apartmentCombo.removeAllItems(); // clear old items

        while (rs.next()) {
            int id = rs.getInt("id");
            String number = rs.getString("apartment_number");
            apartmentCombo.addItem(id + " - " + number); // show id and number
        }

    } catch (SQLException ex) {
        System.out.println("Load Apartments Error: " + ex.getMessage());
    }
}


