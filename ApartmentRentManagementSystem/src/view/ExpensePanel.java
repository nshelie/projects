    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
                                       
   try {
        // Get selected apartment
        String apartmentSelected = (String) apartmentCombo.getSelectedItem();
        if (apartmentSelected == null || apartmentSelected.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select an apartment.");
            return;
        }
        int apartmentId = Integer.parseInt(apartmentSelected.split(" - ")[0]);

        // Get category
        String category = (String) categoryCombo.getSelectedItem();
        if (category == null || category.isEmpty() || category.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a category.");
            return;
        }

        // Get amount
        if (amountTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter an amount.");
            return;
        }

        double amount;
        try {
            amount = Double.parseDouble(amountTxt.getText().trim());
            if (amount <= 0) {
                JOptionPane.showMessageDialog(this, "Amount must be greater than 0.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid amount. Please enter a number.");
            return;
        }

        // Get description
        String description = descriptionTxt.getText().trim();
        if (description.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a description.");
            return;
        }

        // Get date
        if (expenseDateChooser.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Please select a date.");
            return;
        }
        LocalDate date = expenseDateChooser.getDate().toInstant()
                .atZone(java.time.ZoneId.systemDefault())
                .toLocalDate();

        // Validation: date should not be in the future
        if (date.isAfter(LocalDate.now())) {
            JOptionPane.showMessageDialog(this, "Expense date cannot be in the future.");
            return;
        }

        // Create Expense object
        Expense expense = new Expense();
        expense.setApartmentId(apartmentId);
        expense.setCategory(category);
        expense.setAmount(amount);
        expense.setDescription(description);
        expense.setDate(date);

        // Save to DB
        if (expenseDao.insertExpense(expense)) {
            JOptionPane.showMessageDialog(this, "Expense saved successfully!");
            loadAllExpenses();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to save expense.");
        }

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Save Expense Error: " + ex.getMessage());
    }
    
       
    }//GEN-LAST:event_saveBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
                                               
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Select a record to delete.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) return;

    try {
        int expenseId = (int) jTable1.getValueAt(selectedRow, 0);
        if (expenseDao.deleteExpense(expenseId)) {
            JOptionPane.showMessageDialog(this, "Expense deleted successfully!");
            loadAllExpenses();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete expense.");
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_deleteBtnActionPerformed

