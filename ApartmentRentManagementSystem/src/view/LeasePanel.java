     private void loadTenants() {
        tenantsCombo.removeAllItems();
        for (Tenant t : tenantDao.getAllTenants()) {
            tenantsCombo.addItem(t.getId() + " - " + t.getFullName());
        }
    }

    private void loadApartments() {
        apartmentsCombo.removeAllItems();
        for (Apartment a : apartmentDao.getAllApartments()) {
            apartmentsCombo.addItem(a.getId() + " - " + a.getApartmentNumber());
        }
    }

    private void loadLeaseTable() {
        ArrayList<Lease> list = leaseDao.getAllLeases();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Lease l : list) {
            model.addRow(new Object[]{
                    l.getId(),
                    l.getTenantName(),
                    l.getApartmentNumber(),
                    l.getRentType(),
                    l.getStartDate(),
                    l.getEndDate(),
                    l.getRentAmount()
            });
        }
    }
    
  
