    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
       try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String startDate = sdf.format(jDateChooser1.getDate());
            String endDate = sdf.format(jDateChooser2.getDate());

            int tenantId = Integer.parseInt(tenantsCombo.getSelectedItem().toString().split(" - ")[0]);
            int apartmentId = Integer.parseInt(apartmentsCombo.getSelectedItem().toString().split(" - ")[0]);

            Lease lease = new Lease();
            lease.setTenantId(tenantId);
            lease.setApartmentId(apartmentId);
            lease.setRentType(rentTypeCombo.getSelectedItem().toString());
            lease.setRentAmount(Double.parseDouble(rentAmountTxt.getText()));
            lease.setStartDate(startDate);
            lease.setEndDate(endDate);
            lease.setIsActive(true);

            if (leaseDao.insertLease(lease)) {
                JOptionPane.showMessageDialog(this, "Lease added successfully!");
                loadLeaseTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add lease.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
            if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Please select a lease to update.");
            return;
        }
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String startDate = sdf.format(jDateChooser1.getDate());
            String endDate = sdf.format(jDateChooser2.getDate());

            int tenantId = Integer.parseInt(tenantsCombo.getSelectedItem().toString().split(" - ")[0]);
            int apartmentId = Integer.parseInt(apartmentsCombo.getSelectedItem().toString().split(" - ")[0]);

            Lease lease = new Lease();
            lease.setId(selectedId);
            lease.setTenantId(tenantId);
            lease.setApartmentId(apartmentId);
            lease.setRentType(rentTypeCombo.getSelectedItem().toString());
            lease.setRentAmount(Double.parseDouble(rentAmountTxt.getText()));
            lease.setStartDate(startDate);
            lease.setEndDate(endDate);

            if (leaseDao.updateLease(lease)) {
                JOptionPane.showMessageDialog(this, "Lease updated successfully!");
                loadLeaseTable();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update lease.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
         if (selectedId == -1) {
            JOptionPane.showMessageDialog(this, "Please select a lease to delete.");
            return;
        }
        if (leaseDao.deleteLease(selectedId)) {
            JOptionPane.showMessageDialog(this, "Lease deleted!");
            loadLeaseTable();
            selectedId = -1;
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete lease.");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

