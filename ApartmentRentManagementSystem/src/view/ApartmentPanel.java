    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed

       String keyword = searchTxt.getText().trim();
        ApartmentDao dao = new ApartmentDao();
        List<Apartment> apartmentList = dao.searchApartments(keyword);

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Apartment a : apartmentList) {
            model.addRow(new Object[]{
                a.getId(),
                a.getApartmentNumber(),
                a.getLocation(),
                a.getRentAmount(),
                a.isOccupied() ? "Occupied" : "Available",
                a.getRent_type()
            });
        }
    }//GEN-LAST:event_searchBtnActionPerformed

  public void loadApartmentTable() {
        ApartmentDao dao = new ApartmentDao();
        List<Apartment> list = dao.getAllApartments();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Apartment a : list) {
            model.addRow(new Object[]{
                a.getId(),
                a.getApartmentNumber(),
                a.getLocation(),
                a.getRentAmount(),
                a.isOccupied() ? "Occupied" : "Available",
                a.getRent_type()
            });
        }
    }

