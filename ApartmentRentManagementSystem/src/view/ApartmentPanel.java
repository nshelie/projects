// ---------- VALIDATION HELPERS ----------
    private boolean isValidApartmentNumberInput(String input) {
        // Accept digits or digits with prefix; we will auto-add N- if user forgot
        // valid pattern after prefix: N- followed by 1+ digits or letters if you allow letters
        if (input == null || input.trim().isEmpty()) return false;
        // allow numbers only in input field (user types digits), or full "N-123"
        String trimmed = input.trim();
        if (trimmed.startsWith("N-")) trimmed = trimmed.substring(2);
        // ensure remaining has at least one digit and contains only letters/digits/hyphen underscore? We'll require digits.
        return Pattern.matches("\\d{1,}", trimmed);
    }

    private String normalizeApartmentNumber(String userInput) {
        String t = userInput.trim();
        if (!t.startsWith("N-")) {
            t = "N-" + t;
        }
        return t;
    }

    private boolean isValidRentAmount(String rentText) {
        if (rentText == null || rentText.trim().isEmpty()) return false;
        try {
            double r = Double.parseDouble(rentText.trim());
            return r > 0; // rent must be positive
        } catch (NumberFormatException ex) {
            return false;
        }
    }

    private boolean isValidLocation(String loc) {
        return loc != null && !loc.trim().isEmpty() && loc.trim().length() >= 2;
    }

