private void clearFields() {
    apartmentNumberTxt.setText("");
    locationTxt.setText("");
    rentAmountTxt.setText("");
    occupiedCombo.setSelectedIndex(0);
    rentTypeCombo.setSelectedIndex(0); // reset rent type too
    searchTxt.setText("");
    selectedId = -1;
}


 private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
        int row = jTable1.getSelectedRow();
        if (row < 0) return;

        selectedId = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

        apartmentNumberTxt.setText(jTable1.getValueAt(row, 1).toString().replaceFirst("^N-", "")); // show without prefix for easy editing
        locationTxt.setText(jTable1.getValueAt(row, 2).toString());
        rentAmountTxt.setText(jTable1.getValueAt(row, 3).toString());
        occupiedCombo.setSelectedItem(jTable1.getValueAt(row, 4).toString());
        // FIXED: set rent type too
        rentTypeCombo.setSelectedItem(jTable1.getValueAt(row, 5).toString());
    }


